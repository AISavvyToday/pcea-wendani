class OutstandingBalancesExcelView(LoginRequiredMixin, View):
    """
    Exports Outstanding Balances (per-student aggregated) to Excel.
    Accepts same GET params as the on-screen report (academic_year, term, start_date, end_date, student_class, balance_operator, balance_amount).
    """

    def get(self, request):
        # Use same filter logic as your OutstandingBalancesReportView
        from .forms import OutstandingBalancesFilterForm  # import here to avoid circular import
        form = OutstandingBalancesFilterForm(request.GET or None)
        if not form.is_valid():
            return HttpResponseBadRequest("Invalid filters")

        # extract filters (same keys used earlier)
        start_date = form.cleaned_data.get('start_date')
        end_date = form.cleaned_data.get('end_date')
        academic_year = form.cleaned_data.get('academic_year')
        term = form.cleaned_data.get('term')
        student_class = form.cleaned_data.get('student_class')
        balance_op = form.cleaned_data.get('balance_operator') or 'any'
        balance_amt = form.cleaned_data.get('balance_amount') or Decimal('0.00')
        include_zero = form.cleaned_data.get('show_zero_balances')

        invoices = Invoice.objects.select_related('student', 'term__academic_year')

        if academic_year:
            invoices = invoices.filter(term__academic_year=academic_year)
            if term:
                invoices = invoices.filter(term__term=term)
        if start_date:
            invoices = invoices.filter(issue_date__gte=start_date)
        if end_date:
            invoices = invoices.filter(issue_date__lte=end_date)
        if student_class:
            invoices = invoices.filter(student__current_class=student_class)

        annotations = {
            'total_billed': Coalesce(Sum('total_amount'), Value(0)),
            'total_paid': Coalesce(Sum('amount_paid'), Value(0)),
            'total_balance': Coalesce(Sum('balance'), Value(0)),
            'total_balance_bf': Coalesce(Sum('balance_bf'), Value(0)),
            'total_prepayment': Coalesce(Sum('prepayment'), Value(0)),
        }

        grouped_qs = invoices.values(
            'student__pk',
            'student__admission_number',
            'student__full_name',
            'student__current_class',
            'student__contacts',
            'term__academic_year__year',
        ).annotate(**annotations).order_by('-total_balance', 'student__full_name')

        # balance filter
        if balance_op != 'any' and balance_amt is not None:
            lookup = {
                '=': 'total_balance',
                '>': 'total_balance__gt',
                '<': 'total_balance__lt',
                '>=': 'total_balance__gte',
                '<=': 'total_balance__lte',
            }.get(balance_op, None)
            if lookup:
                grouped_qs = grouped_qs.filter(**{lookup: balance_amt})

        if not include_zero:
            grouped_qs = grouped_qs.exclude(total_balance=Decimal('0.00'))

        rows = list(grouped_qs)

        # Build Excel
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Outstanding Balances"

        add_common_header(ws, "Outstanding Balances Report")

        headers = ['Year', 'Admission No', 'Name', 'Class', 'Contacts', 'Prepayment', 'Balance B/F', 'Balance', 'Paid', 'Total Billed']
        for c, h in enumerate(headers, start=1):
            cell = ws.cell(row=5, column=c, value=h)
            cell.font = Font(bold=True)

        row_num = 6
        for r in rows:
            ws.cell(row=row_num, column=1, value=r.get('term__academic_year__year'))
            ws.cell(row=row_num, column=2, value=r.get('student__admission_number'))
            ws.cell(row=row_num, column=3, value=r.get('student__full_name'))
            ws.cell(row=row_num, column=4, value=r.get('student__current_class'))
            ws.cell(row=row_num, column=5, value=r.get('student__contacts') or "")
            c6 = ws.cell(row=row_num, column=6, value=float(r.get('total_prepayment') or 0))
            format_money_cell(c6)
            c7 = ws.cell(row=row_num, column=7, value=float(r.get('total_balance_bf') or 0))
            format_money_cell(c7)
            c8 = ws.cell(row=row_num, column=8, value=float(r.get('total_balance') or 0))
            format_money_cell(c8)
            c9 = ws.cell(row=row_num, column=9, value=float(r.get('total_paid') or 0))
            format_money_cell(c9)
            c10 = ws.cell(row=row_num, column=10, value=float(r.get('total_billed') or 0))
            format_money_cell(c10)
            row_num += 1

        # Totals row
        totals = {
            'total_prepayment': sum((r['total_prepayment'] or Decimal('0.00')) for r in rows),
            'total_balance_bf': sum((r['total_balance_bf'] or Decimal('0.00')) for r in rows),
            'total_balance': sum((r['total_balance'] or Decimal('0.00')) for r in rows),
            'total_paid': sum((r['total_paid'] or Decimal('0.00')) for r in rows),
            'total_billed': sum((r['total_billed'] or Decimal('0.00')) for r in rows),
        }
        ws.cell(row=row_num, column=1, value='Totals').font = Font(bold=True)
        c6 = ws.cell(row=row_num, column=6, value=float(totals['total_prepayment']))
        format_money_cell(c6)
        c7 = ws.cell(row=row_num, column=7, value=float(totals['total_balance_bf']))
        format_money_cell(c7)
        c8 = ws.cell(row=row_num, column=8, value=float(totals['total_balance']))
        format_money_cell(c8)
        c9 = ws.cell(row=row_num, column=9, value=float(totals['total_paid']))
        format_money_cell(c9)
        c10 = ws.cell(row=row_num, column=10, value=float(totals['total_billed']))
        format_money_cell(c10)

        # Auto width columns
        for col in range(1, len(headers) + 1):
            ws.column_dimensions[get_column_letter(col)].width = 18

        bytes_data = workbook_to_bytes(wb)
        filename = f"outstanding-balances-{datetime.now().strftime('%Y%m%d-%H%M')}.xlsx"
        return xlsx_response(bytes_data, filename)


