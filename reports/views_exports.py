# ---------- Helpers ----------
def xlsx_response(workbook_bytes, filename):
    response = HttpResponse(workbook_bytes, content_type='application/vnd.openxmlformats-officedocument.spreadsheetml.sheet')
    response['Content-Disposition'] = f'attachment; filename="{filename}"'
    return response


def workbook_to_bytes(wb):
    bio = io.BytesIO()
    wb.save(bio)
    bio.seek(0)
    return bio.getvalue()


def format_money_cell(cell):
    # Use Kenyan Shilling formatting as example; change to suits you
    cell.number_format = '#,##0.00'


def add_common_header(ws, title):
    # Title row
    ws.merge_cells('A1:G1')
    ws['A1'] = getattr(settings, 'SCHOOL_NAME', 'School')
    ws['A1'].font = Font(size=14, bold=True)
    ws['A1'].alignment = Alignment(horizontal='center')
    ws.merge_cells('A2:G2')
    ws['A2'] = title
    ws['A2'].font = Font(size=12, bold=True)
    ws['A2'].alignment = Alignment(horizontal='center')
    # small meta row
    ws.merge_cells('A3:G3')
    ws['A3'] = f'Generated by: {""} on {datetime.now().strftime("%d %b %Y %H:%M")}'
    ws['A3'].font = Font(size=9)
    ws['A3'].alignment = Alignment(horizontal='center')


